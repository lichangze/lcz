package com.bmzy.report.model;

import java.util.Date;

import javax.persistence.Column;
import javax.persistence.Table;

@Table(name = "interface.ZD_SECURITYCHECK")
public class SecurityCheckEntity {
	
	@Column(name = "ID")
	private String id;
	@Column(name = "LINEID")
	private String lineId;
	@Column(name = "CDATE")
	private Date cDate;
	@Column(name = "PEOPLE")
	private String people;
	@Column(name = "GUN")
	private String gun;
	@Column(name = "MILITARY")
	private String military;
	@Column(name = "BOMB")
	private String bomb;
	@Column(name = "BULLETE")
	private String bullete;
	@Column(name = "KNIFE")
	private String knife;
	@Column(name = "BURN")
	private String burn;
	@Column(name = "OTHER")
	private String other;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getLineId() {
		return lineId;
	}
	public void setLineId(String lineId) {
		this.lineId = lineId;
	}
	public Date getcDate() {
		return cDate;
	}
	public void setcDate(Date cDate) {
		this.cDate = cDate;
	}
	public String getPeople() {
		return people;
	}
	public void setPeople(String people) {
		this.people = people;
	}
	public String getGun() {
		return gun;
	}
	public void setGun(String gun) {
		this.gun = gun;
	}
	public String getMilitary() {
		return military;
	}
	public void setMilitary(String military) {
		this.military = military;
	}
	public String getBomb() {
		return bomb;
	}
	public void setBomb(String bomb) {
		this.bomb = bomb;
	}
	public String getBullete() {
		return bullete;
	}
	public void setBullete(String bullete) {
		this.bullete = bullete;
	}
	public String getKnife() {
		return knife;
	}
	public void setKnife(String knife) {
		this.knife = knife;
	}
	public String getBurn() {
		return burn;
	}
	public void setBurn(String burn) {
		this.burn = burn;
	}
	public String getOther() {
		return other;
	}
	public void setOther(String other) {
		this.other = other;
	}
	
	


}
