package com.bmzy.report.model;

import javax.persistence.Column;
import javax.persistence.Id;
import javax.persistence.Table;

@Table(name = "interface.VW_TRANSPORT_CAPACITY")
public class VWTranSportCapacityEntityy {
	@Column(name = "SECTIONNUM")
	private int sectionnum;
	@Id
	@Column(name = "CDATE")
	private String cdate;
	@Id
	@Column(name = "Times")
	private String Times;
	@Column(name = "SECTIONNAME")
	private String sectionname;
	@Id
	@Column(name = "TRANSD")
	private int transd;
	@Column(name = "TRANSPORTNUM")
	private int transportnum;
	@Column(name = "TRANSPORT")
	private int  transport;
	@Id
	@Column(name = "TRANSCODE")
	private String transcode;
	@Id
	@Column(name = "LINEID")
	private int lineid;
	@Column(name = "Capacity")
	private int capacity;
	@Id
	@Column(name = "SECTIONID")
	private int sectionid;
	public int getSectionnum() {
		return sectionnum;
	}
	public void setSectionnum(int sectionnum) {
		this.sectionnum = sectionnum;
	}
	public String getCdate() {
		return cdate;
	}
	public void setCdate(String cdate) {
		this.cdate = cdate;
	}
	public String getTimes() {
		return Times;
	}
	public void setTimes(String times) {
		Times = times;
	}
	public String getSectionname() {
		return sectionname;
	}
	public void setSectionname(String sectionname) {
		this.sectionname = sectionname;
	}
	public int getTransd() {
		return transd;
	}
	public void setTransd(int transd) {
		this.transd = transd;
	}
	public int getTransportnum() {
		return transportnum;
	}
	public void setTransportnum(int transportnum) {
		this.transportnum = transportnum;
	}
	public int getTransport() {
		return transport;
	}
	public void setTransport(int transport) {
		this.transport = transport;
	}
	public String getTranscode() {
		return transcode;
	}
	public void setTranscode(String transcode) {
		this.transcode = transcode;
	}
	public int getLineid() {
		return lineid;
	}
	public void setLineid(int lineid) {
		this.lineid = lineid;
	}
	public int getCapacity() {
		return capacity;
	}
	public void setCapacity(int capacity) {
		this.capacity = capacity;
	}
	public int getSectionid() {
		return sectionid;
	}
	public void setSectionid(int sectionid) {
		this.sectionid = sectionid;
	}
	
	
}
