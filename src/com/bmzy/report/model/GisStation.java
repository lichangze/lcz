package com.bmzy.report.model;

import javax.persistence.Column;
import javax.persistence.Id;
import javax.persistence.Table;

@Table(name = "meta.gis_station")
public class GisStation {

	@Id
	@Column(name = "SECTIONID")
	private String sectionId;
	@Column(name = "STATIONID1")
	private String sectionId1;
	@Column(name = "STATIONID1")
	private String sectionId2;
	@Column(name = "LINEID")
	private String lineId;
	@Column(name = "TRANSD")
	private String transd;
	@Column(name = "SECTIONNAME")
	private String sectionName;
	@Column(name = "ORDERID")
	private Integer orderId;
	@Column(name = "LONGITUDE")
	private float longitude;
	@Column(name = "LATITUDE")
	private float latitude;
	@Column(name = "VALUE")
	private double value;
	@Column(name = "CHANGEVALUE")
	private String changeVale;
	@Column(name = "TYPE")
	private String type;
	@Column(name = "TYPEVALUELON")
	private float typeValuelon;
	@Column(name = "TYPEVALUELAT")
	private float typeValuelat;

	public String getSectionId() {
		return sectionId;
	}

	public void setSectionId(String sectionId) {
		this.sectionId = sectionId;
	}

	public String getSectionId1() {
		return sectionId1;
	}

	public void setSectionId1(String sectionId1) {
		this.sectionId1 = sectionId1;
	}

	public String getSectionId2() {
		return sectionId2;
	}

	public void setSectionId2(String sectionId2) {
		this.sectionId2 = sectionId2;
	}

	public String getLineId() {
		return lineId;
	}

	public void setLineId(String lineId) {
		this.lineId = lineId;
	}

	public String getTransd() {
		return transd;
	}

	public void setTransd(String transd) {
		this.transd = transd;
	}

	public String getSectionName() {
		return sectionName;
	}

	public void setSectionName(String sectionName) {
		this.sectionName = sectionName;
	}

	public Integer getOrderId() {
		return orderId;
	}

	public void setOrderId(Integer orderId) {
		this.orderId = orderId;
	}

	public float getLongitude() {
		return longitude;
	}

	public void setLongitude(float longitude) {
		this.longitude = longitude;
	}

	public float getLatitude() {
		return latitude;
	}

	public void setLatitude(float latitude) {
		this.latitude = latitude;
	}

	public double getValue() {
		return value;
	}

	public void setValue(double value) {
		this.value = value;
	}

	public String getChangeVale() {
		return changeVale;
	}

	public void setChangeVale(String changeVale) {
		this.changeVale = changeVale;
	}

	
	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public float getTypeValuelon() {
		return typeValuelon;
	}

	public void setTypeValuelon(float typeValuelon) {
		this.typeValuelon = typeValuelon;
	}

	public float getTypeValuelat() {
		return typeValuelat;
	}

	public void setTypeValuelat(float typeValuelat) {
		this.typeValuelat = typeValuelat;
	}

	
	
	
	
}
