package com.bmzy.report.model;

import javax.persistence.Column;
import javax.persistence.Id;
import javax.persistence.Table;

@Table(name="qdzd.OD_OtherSituation_MultiSelect")
public class OtherSituationMultiSelectEntity {
	@Id
	@Column(name="MultiSelectID")
	private String MultiSelectID;
	@Column(name="DevFailID")
	private String DevFailID;
	@Column(name="DictionaryItemCode")
	private String DictionaryItemCode;
	@Column(name="DictionaryItemName")
	private String DictionaryItemName;
	@Column(name="SelectType")
	private String SelectType;
	@Column(name="TripNum")
	private String TripNum;
	@Column(name="TripCount")
	private String TripCount;
	@Column(name="DetailRemark")
	private String DetailRemark;
	public String getMultiSelectID() {
		return MultiSelectID;
	}
	public void setMultiSelectID(String multiSelectID) {
		MultiSelectID = multiSelectID;
	}
	public String getDevFailID() {
		return DevFailID;
	}
	public void setDevFailID(String devFailID) {
		DevFailID = devFailID;
	}
	public String getDictionaryItemCode() {
		return DictionaryItemCode;
	}
	public void setDictionaryItemCode(String dictionaryItemCode) {
		DictionaryItemCode = dictionaryItemCode;
	}
	public String getDictionaryItemName() {
		return DictionaryItemName;
	}
	public void setDictionaryItemName(String dictionaryItemName) {
		DictionaryItemName = dictionaryItemName;
	}
	public String getSelectType() {
		return SelectType;
	}
	public void setSelectType(String selectType) {
		SelectType = selectType;
	}
	public String getTripNum() {
		return TripNum;
	}
	public void setTripNum(String tripNum) {
		TripNum = tripNum;
	}
	public String getTripCount() {
		return TripCount;
	}
	public void setTripCount(String tripCount) {
		TripCount = tripCount;
	}
	public String getDetailRemark() {
		return DetailRemark;
	}
	public void setDetailRemark(String detailRemark) {
		DetailRemark = detailRemark;
	}
	
}
