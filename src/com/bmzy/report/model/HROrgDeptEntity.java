package com.bmzy.report.model;

import java.sql.Timestamp;

import javax.persistence.Column;
import javax.persistence.Id;
import javax.persistence.Table;
@Table(name = "HR_ORG_DEPT")
public class HROrgDeptEntity {
	@Column(name = "DEPT_ID")
	private String dept_id;
	@Column(name = "COMP_NO")
	private int comp_no;
	@Id
	@Column(name = "DEPT_NO")
	private String dept_no;
	@Column(name = "DEPT_NAME")
	private String dept_name;
	@Column(name = "P_DEPT_NO")
	private String p_dept_no;
	@Column(name = "DISP_SN")
	private int disp_sn;
	@Column(name = "EMP_NO")
	private String emp_no;
	@Column(name = "ISSEALUP")
	private String issealup;
	@Column(name = "LEVEL")
	private int level;
	@Column(name = "CREATETIME")
	private Timestamp createtime;
	@Column(name = "LASTUPDATETIME")
	private Timestamp lastupdatetime;
	@Column(name = "MEMO")
	private String memo;
	@Column(name = "PARENT_DEPT_NO")
	private String parent_dept_no;
	public String getDept_id() {
		return dept_id;
	}
	public void setDept_id(String dept_id) {
		this.dept_id = dept_id;
	}
	public int getComp_no() {
		return comp_no;
	}
	public void setComp_no(int comp_no) {
		this.comp_no = comp_no;
	}
	public String getDept_no() {
		return dept_no;
	}
	public void setDept_no(String dept_no) {
		this.dept_no = dept_no;
	}
	public String getDept_name() {
		return dept_name;
	}
	public void setDept_name(String dept_name) {
		this.dept_name = dept_name;
	}
	public String getP_dept_no() {
		return p_dept_no;
	}
	public void setP_dept_no(String p_dept_no) {
		this.p_dept_no = p_dept_no;
	}
	public int getDisp_sn() {
		return disp_sn;
	}
	public void setDisp_sn(int disp_sn) {
		this.disp_sn = disp_sn;
	}
	public String getEmp_no() {
		return emp_no;
	}
	public void setEmp_no(String emp_no) {
		this.emp_no = emp_no;
	}
	public String getIssealup() {
		return issealup;
	}
	public void setIssealup(String issealup) {
		this.issealup = issealup;
	}
	public int getLevel() {
		return level;
	}
	public void setLevel(int level) {
		this.level = level;
	}
	public Timestamp getCreatetime() {
		return createtime;
	}
	public void setCreatetime(Timestamp createtime) {
		this.createtime = createtime;
	}
	public Timestamp getLastupdatetime() {
		return lastupdatetime;
	}
	public void setLastupdatetime(Timestamp lastupdatetime) {
		this.lastupdatetime = lastupdatetime;
	}
	public String getMemo() {
		return memo;
	}
	public void setMemo(String memo) {
		this.memo = memo;
	}
	public String getParent_dept_no() {
		return parent_dept_no;
	}
	public void setParent_dept_no(String parent_dept_no) {
		this.parent_dept_no = parent_dept_no;
	}

}
