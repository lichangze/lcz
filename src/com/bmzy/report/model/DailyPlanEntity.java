package com.bmzy.report.model;

import java.util.Date;

import javax.persistence.Column;
import javax.persistence.Id;
import javax.persistence.Table;
@Table(name = "interface.ZD_DAILYPLAN")
public class DailyPlanEntity {
	@Id
	@Column(name = "ID")
	private String id;
	@Column(name = "LINEID")
	private String lineId;
	@Column(name = "CDATE")
    private Date cDate;
	@Column(name = "PLAN")
    private String plan;
	@Column(name = "FACT")
    private String fact;
	@Column(name = "TEMP")
    private String temp;
	@Column(name = "EMPTY")
    private String empty;
	@Column(name = "RESCUE")
    private String rescue;
	@Column(name = "DAYDEBUG")
    private String dayDebug;
	@Column(name = "NIGHTDEBUG")
    private String nightDebug;
	@Column(name = "DEBUG")
    private String deBug;
	@Column(name = "TRAIN")
    private String train;
	@Column(name = "COTRAIN")
    private String coTrain;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getLineId() {
		return lineId;
	}
	public void setLineId(String lineId) {
		this.lineId = lineId;
	}
	public Date getcDate() {
		return cDate;
	}
	public void setcDate(Date cDate) {
		this.cDate = cDate;
	}
	public String getPlan() {
		return plan;
	}
	public void setPlan(String plan) {
		this.plan = plan;
	}
	public String getFact() {
		return fact;
	}
	public void setFact(String fact) {
		this.fact = fact;
	}
	public String getTemp() {
		return temp;
	}
	public void setTemp(String temp) {
		this.temp = temp;
	}
	public String getEmpty() {
		return empty;
	}
	public void setEmpty(String empty) {
		this.empty = empty;
	}
	public String getRescue() {
		return rescue;
	}
	public void setRescue(String rescue) {
		this.rescue = rescue;
	}
	public String getDayDebug() {
		return dayDebug;
	}
	public void setDayDebug(String dayDebug) {
		this.dayDebug = dayDebug;
	}
	public String getNightDebug() {
		return nightDebug;
	}
	public void setNightDebug(String nightDebug) {
		this.nightDebug = nightDebug;
	}
	public String getDeBug() {
		return deBug;
	}
	public void setDeBug(String deBug) {
		this.deBug = deBug;
	}
	public String getTrain() {
		return train;
	}
	public void setTrain(String train) {
		this.train = train;
	}
	public String getCoTrain() {
		return coTrain;
	}
	public void setCoTrain(String coTrain) {
		this.coTrain = coTrain;
	}
    

}
